<?php

namespace RiftRunBundle\Model;

/**
 * Grift
 */
class Grift extends GameType
{
    /**
     * @var integer
     */
    private $id;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }
    /**
     * @var string
     */
    private $level;


    /**
     * Set level
     *
     * @param string $level
     *
     * @return Grift
     */
    public function setLevel($level)
    {
        $this->level = $level;

        return $this;
    }

    /**
     * Get level
     *
     * @return string
     */
    public function getLevel()
    {
        return $this->level;
    }
}
